--1. Criação das tabelas
CREATE TABLE TB_MAQUINAS (
    ID_MAQUINA    VARCHAR2(20) PRIMARY KEY, -- Ex: TRT-001
    MODELO        VARCHAR2(50) NOT NULL,
    FABRICANTE    VARCHAR2(50),
    HORIMETRO_REV NUMBER(10, 2) NOT NULL,  -- Próxima revisão (ex: 500.00)
    STATUS_ATUAL  VARCHAR2(20) DEFAULT 'OPERACIONAL'
);
CREATE TABLE TB_TELEMETRIA (
    ID_LEITURA    NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ID_MAQUINA    VARCHAR2(20) REFERENCES TB_MAQUINAS(ID_MAQUINA),
    TEMP_MOTOR    NUMBER(5, 2), -- Ex: 92.50
    PRESSAO_HIDR  NUMBER(5, 2), -- Ex: 180.00
    HORIMETRO_ATU NUMBER(10, 2),
    DATA_LEITURA  TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    ALERTA_GERADO VARCHAR2(10)  -- 'SIM' ou 'NAO'
);


